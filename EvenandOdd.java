import java.util.scanner;
class EvenandOdd{
	public static void main(String[] args){
		Scanner s = new Scanner(System.in);
		n = s.nextInt();
		int num = n;
		if (num % 2 ==0){
			System.out.println("num is even");
		}
		else if (num % 2 != 0){
			System.out.println("num is odd");
		}
		else {
			System.out.println("Entered number is even or odd");
			}
	
	}
}